/**
 * @brief Helper: simulate nRF24 TX-side pipeline (whiten + swapbits).
 *
 * Takes BLE-format plaintext, applies Galois LFSR whitening (pure XOR,
 * no bit-swap), then bit-swaps each byte to produce nRF24-format output.
 * This is what the nRF24 would receive over-the-air and store in its FIFO.
 *
 * @param plaintext  BLE-format PDU bytes (LSbit-first on-air order).
 * @param len        Number of bytes.
 * @param ch         BLE channel index (0–39).
 * @param out        Output buffer (must be at least len bytes).
 */
static void simulate_nrf24_tx(const uint8_t *plaintext, uint8_t len,
                               uint8_t ch, uint8_t *out)
{
    /* Step 1: Galois LFSR whitening (same as dewhiten step 2 — XOR is symmetric) */
    uint8_t lfsr = swapbits(ch) | 0x02;
    for (uint8_t i = 0; i < len; i++) {
        uint8_t w = 0;
        for (uint8_t m = 1; m; m <<= 1) {
            if (lfsr & 0x80) {
                lfsr ^= 0x11;
                w ^= m;
            }
            lfsr <<= 1;
        }
        out[i] = plaintext[i] ^ w;
    }
    /* Step 2: bit-swap (BLE LSbit-first → nRF24 MSbit-first) */
    for (uint8_t i = 0; i < len; i++) {
        out[i] = swapbits(out[i]);
    }
}

/** @brief AC-4: Round-trip on BLE advertising channels 37, 38, 39. */
TEST(Dewhiten, RoundTrip_AdvChannels) {
    const uint8_t original[] = {0x40, 0x0B, 0xEF, 0xFF, 0xC0, 0xAA, 0x18, 0x00};
    uint8_t buf[8];
    uint8_t nrf24[8];

    for (uint8_t ch : {37, 38, 39}) {
        /* Simulate nRF24 TX: whiten then swapbits */
        simulate_nrf24_tx(original, 8, ch, nrf24);
        /* Copy into mutable buffer for dewhiten */
        std::memcpy(buf, nrf24, 8);
        /* dewhiten: swapbits then LFSR XOR → should recover original */
        dewhiten(buf, 8, ch);
        for (int i = 0; i < 8; i++) {
            EXPECT_EQ(buf[i], original[i])
                << "ch=" << (int)ch << " byte " << i
                << ": got 0x" << std::hex << (int)buf[i]
                << " expected 0x" << (int)original[i];
        }
    }
}

