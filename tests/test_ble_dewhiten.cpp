/**
 * @file test_ble_dewhiten.cpp
 * @brief Host-side unit tests for nrf24::ble::swapbits() and dewhiten().
 *
 * Validates the BLE data dewhitening implementation against the following
 * acceptance criteria:
 *
 *   AC-1  swapbits() correctly reverses bit order within a byte
 *   AC-2  swapbits() is an involution: swapbits(swapbits(x)) == x
 *   AC-3  dewhiten() produces correct output for known whitened vectors
 *         (cross-verified against Dmitry Grinberg's Python reference)
 *   AC-4  dewhiten() round-trips: original → whiten → swapbits → dewhiten → original
 *   AC-5  dewhiten() is channel-dependent (different channels produce different output)
 *   AC-6  dewhiten() handles edge cases: zero-length, single-byte, all-zero,
 *         all-0xFF, maximum 32-byte payloads
 *   AC-7  dewhiten() on all-zero input yields the raw whitening sequence bytes
 *   AC-8  Full RX pipeline: bit-swap + dewhiten recovers original BLE PDU
 *   AC-9  After dewhitening, PDU header fields are valid
 *
 * Known vectors generated by docs/pipeline/scripts/ble_whiten_reference.py
 * which implements Dmitry Grinberg's btLeWhiten exactly.
 *
 * @see nrf24::ble::swapbits()  — bit reversal (ble.h)
 * @see nrf24::ble::dewhiten()  — Galois LFSR dewhitening (ble.cpp)
 * @see docs/pipeline/scripts/ble_whiten_reference.py
 */

