/** @brief Verify swapbits on identity values: all-zeros and all-ones stay unchanged. */
TEST(Swapbits, IdentityValues) {
    EXPECT_EQ(swapbits(0x00), 0x00);
    EXPECT_EQ(swapbits(0xFF), 0xFF);
}

/** @brief Verify swapbits on palindromic bytes (bit pattern reads the same forwards and backwards).
 *
 * A byte is bit-palindromic when swapbits(v) == v, i.e., bit[i] == bit[7-i]
 * for all i.  There are exactly 16 such values among all uint8_t.
 */
TEST(Swapbits, PalindromicBytes) {
    EXPECT_EQ(swapbits(0x00), 0x00);  /* 0b00000000 */
    EXPECT_EQ(swapbits(0x18), 0x18);  /* 0b00011000 */
    EXPECT_EQ(swapbits(0x24), 0x24);  /* 0b00100100 */
    EXPECT_EQ(swapbits(0x3C), 0x3C);  /* 0b00111100 */
    EXPECT_EQ(swapbits(0x42), 0x42);  /* 0b01000010 */
    EXPECT_EQ(swapbits(0x5A), 0x5A);  /* 0b01011010 */
    EXPECT_EQ(swapbits(0x66), 0x66);  /* 0b01100110 */
    EXPECT_EQ(swapbits(0x7E), 0x7E);  /* 0b01111110 */
    EXPECT_EQ(swapbits(0x81), 0x81);  /* 0b10000001 */
    EXPECT_EQ(swapbits(0x99), 0x99);  /* 0b10011001 */
    EXPECT_EQ(swapbits(0xA5), 0xA5);  /* 0b10100101 */
    EXPECT_EQ(swapbits(0xBD), 0xBD);  /* 0b10111101 */
    EXPECT_EQ(swapbits(0xC3), 0xC3);  /* 0b11000011 */
    EXPECT_EQ(swapbits(0xDB), 0xDB);  /* 0b11011011 */
    EXPECT_EQ(swapbits(0xE7), 0xE7);  /* 0b11100111 */
    EXPECT_EQ(swapbits(0xFF), 0xFF);  /* 0b11111111 */
}

/** @brief Verify swapbits on known non-palindromic byte reversals. */
TEST(Swapbits, KnownReversals) {
    EXPECT_EQ(swapbits(0x01), 0x80);  /* bit 0 → bit 7 */
    EXPECT_EQ(swapbits(0x80), 0x01);  /* bit 7 → bit 0 */
    EXPECT_EQ(swapbits(0x0F), 0xF0);  /* low nibble → high nibble */
    EXPECT_EQ(swapbits(0xF0), 0x0F);  /* high nibble → low nibble */
    EXPECT_EQ(swapbits(0x02), 0x40);  /* bit 1 → bit 6 */
    EXPECT_EQ(swapbits(0x04), 0x20);  /* bit 2 → bit 5 */
    EXPECT_EQ(swapbits(0x08), 0x10);  /* bit 3 → bit 4 */
    EXPECT_EQ(swapbits(0x10), 0x08);  /* bit 4 → bit 3 */
    EXPECT_EQ(swapbits(0x20), 0x04);  /* bit 5 → bit 2 */
    EXPECT_EQ(swapbits(0x40), 0x02);  /* bit 6 → bit 1 */
}

/** @brief Verify swapbits is an involution for selected edge-case values. */
TEST(Swapbits, Involution_EdgeCases) {
    /* swapbits(swapbits(x)) == x for edge cases */
    uint8_t const edges[] = {0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80,
                              0x0F, 0xF0, 0x55, 0xAA, 0x42, 0xFF, 0xDE, 0xAD, 0xBE, 0xEF};
    for (auto v : edges) {
        EXPECT_EQ(swapbits(swapbits(v)), v)
            << "swapbits(swapbits(0x" << std::hex << (int)v << ")) != 0x" << (int)v;
    }
}

/** @brief Verify swapbits is an involution for ALL 256 uint8_t values. */
TEST(Swapbits, Involution_All256) {
    for (int i = 0; i <= 0xFF; i++) {
        uint8_t v = static_cast<uint8_t>(i);
        EXPECT_EQ(swapbits(swapbits(v)), v)
            << "swapbits(swapbits(0x" << std::hex << i << ")) failed";
    }
}

