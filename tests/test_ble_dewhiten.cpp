/** @brief AC-5: dewhitening with different channels produces different results.
 *
 * Proves the LFSR seed is channel-dependent. Same input, different channel →
 * different output.
 */
TEST(Dewhiten, CrossChannelIndependence) {
    const uint8_t test_input[] = {0x12, 0x34, 0x56, 0x78};
    uint8_t result_37[4], result_38[4], result_39[4];

    std::memcpy(result_37, test_input, 4);
    std::memcpy(result_38, test_input, 4);
    std::memcpy(result_39, test_input, 4);

    dewhiten(result_37, 4, 37);
    dewhiten(result_38, 4, 38);
    dewhiten(result_39, 4, 39);

    /* All three results must be pairwise different */
    bool same_37_38 = (std::memcmp(result_37, result_38, 4) == 0);
    bool same_37_39 = (std::memcmp(result_37, result_39, 4) == 0);
    bool same_38_39 = (std::memcmp(result_38, result_39, 4) == 0);

    EXPECT_FALSE(same_37_38) << "ch37 and ch38 produced identical dewhiten output";
    EXPECT_FALSE(same_37_39) << "ch37 and ch39 produced identical dewhiten output";
    EXPECT_FALSE(same_38_39) << "ch38 and ch39 produced identical dewhiten output";
}

